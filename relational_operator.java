public class relational_operator {
    public static void main(String[] args) {
        int a=12,b=15;
        if(a==b){
            System.out.println("a==b");

        }
        else{
            System.out.println("a!=b");
        }
        if(a<b){
            System.out.println("a<b");
        }
        else{
            System.out.println("a>b");
        }
        if(a<=b){
            System.out.println("a<=b");
        }
        if(a>=b){
            System.out.println("a>=b");
        }
    }
}
